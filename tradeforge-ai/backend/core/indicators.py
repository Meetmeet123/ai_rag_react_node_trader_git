def bollinger_bands(
    series: pd.Series,
    period: int = 20,
    std_dev: float = 2.0,
) -> Tuple[pd.Series, pd.Series, pd.Series]:
    """Bollinger Bands (BB).

    Parameters
    ----------
    series:
        Input price series.
    period:
        SMA look-back (default 20).
    std_dev:
        Number of standard deviations (default 2.0).

    Returns
    -------
    tuple[pd.Series, pd.Series, pd.Series]
        ``(upper_band, middle_band, lower_band)``
    """
    middle = sma(series, period)
    std = series.rolling(window=period, min_periods=period).std()
    upper = middle + std * std_dev
    lower = middle - std * std_dev
    return upper, middle, lower


def atr(
    high: pd.Series,
    low: pd.Series,
    close: pd.Series,
    period: int = 14,
) -> pd.Series:
    """Average True Range (ATR) — volatility measure.

    Parameters
    ----------
    high, low, close:
        OHLC series.
    period:
        Look-back window (default 14).

    Returns
    -------
    pd.Series
        ATR values (in price units).
    """
    tr1 = high - low
    tr2 = (high - close.shift(1)).abs()
    tr3 = (low - close.shift(1)).abs()
    tr = pd.concat([tr1, tr2, tr3], axis=1).max(axis=1)
    return tr.rolling(window=period, min_periods=period).mean()


def keltner_channels(
    high: pd.Series,
    low: pd.Series,
    close: pd.Series,
    period: int = 20,
    atr_multiplier: float = 2.0,
) -> Tuple[pd.Series, pd.Series, pd.Series]:
    """Keltner Channels — volatility-based envelopes.

    Parameters
    ----------
    high, low, close:
        OHLC series.
    period:
        EMA look-back for the middle line (default 20).
    atr_multiplier:
        ATR multiplier for channel width (default 2.0).

    Returns
    -------
    tuple[pd.Series, pd.Series, pd.Series]
        ``(upper_channel, middle_line, lower_channel)``
    """
    middle = ema(close, period)
    atr_vals = atr(high, low, close, period)
    upper = middle + atr_multiplier * atr_vals
    lower = middle - atr_multiplier * atr_vals
    return upper, middle, lower


def supertrend(
    high: pd.Series,
    low: pd.Series,
    close: pd.Series,
    period: int = 10,
    multiplier: float = 3.0,
) -> Tuple[pd.Series, pd.Series]:
    """SuperTrend indicator — simplified upper/lower bands.

    Parameters
    ----------
    high, low, close:
        OHLC series.
    period:
        ATR look-back (default 10).
    multiplier:
        ATR multiplier for band width (default 3.0).

    Returns
    -------
    tuple[pd.Series, pd.Series]
        ``(upper_band, lower_band)``
    """
    hl2 = (high + low) / 2.0
    atr_vals = atr(high, low, close, period)
    upper_band = hl2 + multiplier * atr_vals
    lower_band = hl2 - multiplier * atr_vals
    return upper_band, lower_band


def psar(
    high: pd.Series,
    low: pd.Series,
    close: pd.Series,
    af: float = 0.02,
    max_af: float = 0.2,
) -> pd.Series:
    """Parabolic SAR (PSAR) — simplified implementation.

    Parameters
    ----------
    high, low, close:
        OHLC series.
    af:
        Initial acceleration factor (default 0.02).
    max_af:
        Maximum acceleration factor (default 0.20).

    Returns
    -------
    pd.Series
        PSAR values aligned to the input index.
    """
    n = len(close)
    psar_vals = pd.Series(index=close.index, dtype=float)
    if n == 0:
        return psar_vals

    # Initialise
    bull = True
    psar_val = float(low.iloc[0])
    ep = float(high.iloc[0])  # Extreme point
    af_val = af

    psar_vals.iloc[0] = psar_val

    for i in range(1, n):
        h = float(high.iloc[i])
        l_val = float(low.iloc[i])

        if bull:
            psar_val = psar_val + af_val * (ep - psar_val)
            # Ensure PSAR is below the last two lows
            psar_val = min(psar_val, float(low.iloc[i - 1]), l_val)
            if h > ep:
                ep = h
                af_val = min(af_val + af, max_af)
            # Bearish reversal
            if l_val < psar_val:
                bull = False
                psar_val = ep
                ep = l_val
                af_val = af
        else:
            psar_val = psar_val + af_val * (ep - psar_val)
            psar_val = max(psar_val, float(high.iloc[i - 1]), h)
            if l_val < ep:
                ep = l_val
                af_val = min(af_val + af, max_af)
            # Bullish reversal
            if h > psar_val:
                bull = True
                psar_val = ep
                ep = h
                af_val = af

        psar_vals.iloc[i] = psar_val

    return psar_vals


