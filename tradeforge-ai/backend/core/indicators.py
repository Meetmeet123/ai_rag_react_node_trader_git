def adx(
    high: pd.Series,
    low: pd.Series,
    close: pd.Series,
    period: int = 14,
) -> pd.Series:
    """Average Directional Index (ADX).

    Measures trend strength regardless of direction.  Values > 25 generally
    indicate a trending market.

    Parameters
    ----------
    high, low, close:
        OHLC series.
    period:
        Look-back window (default 14).

    Returns
    -------
    pd.Series
        ADX values ranging from 0 to 100.
    """
    # True Range
    tr1 = high - low
    tr2 = (high - close.shift(1)).abs()
    tr3 = (low - close.shift(1)).abs()
    tr = pd.concat([tr1, tr2, tr3], axis=1).max(axis=1)

    atr_vals = tr.rolling(window=period, min_periods=period).mean()

    # Directional Movement
    plus_dm = high.diff()
    minus_dm = -low.diff()

    plus_dm = plus_dm.where((plus_dm > minus_dm) & (plus_dm > 0.0), 0.0)
    minus_dm = minus_dm.where((minus_dm > plus_dm) & (minus_dm > 0.0), 0.0)

    plus_di = 100.0 * plus_dm.rolling(window=period, min_periods=period).mean() / atr_vals.replace(0.0, np.nan)
    minus_di = 100.0 * minus_dm.rolling(window=period, min_periods=period).mean() / atr_vals.replace(0.0, np.nan)

    dx = 100.0 * (plus_di - minus_di).abs() / (plus_di + minus_di).replace(0.0, np.nan)
    adx_vals = dx.rolling(window=period, min_periods=period).mean()
    return adx_vals


