def rsi(series: pd.Series, period: int = 14) -> pd.Series:
    """Relative Strength Index (RSI).

    Classic 0-100 momentum oscillator measuring speed / magnitude of
    price movements.

    Parameters
    ----------
    series:
        Input price series (typically close).
    period:
        Look-back window (default 14).

    Returns
    -------
    pd.Series
        RSI values in the range [0, 100].  Leading ``period`` values are
        ``NaN``.
    """
    if period < 1:
        raise ValueError(f"period must be >= 1, got {period}")
    delta = series.diff()
    gain = delta.where(delta > 0, 0.0)
    loss = (-delta).where(delta < 0, 0.0)

    avg_gain = gain.rolling(window=period, min_periods=period).mean()
    avg_loss = loss.rolling(window=period, min_periods=period).mean()

    rs = avg_gain / avg_loss
    rsi_vals = 100.0 - (100.0 / (1.0 + rs))
    return rsi_vals


def macd(
    series: pd.Series,
    fast: int = 12,
    slow: int = 26,
    signal: int = 9,
) -> Tuple[pd.Series, pd.Series, pd.Series]:
    """Moving Average Convergence Divergence (MACD).

    Parameters
    ----------
    series:
        Input price series.
    fast:
        Fast EMA span (default 12).
    slow:
        Slow EMA span (default 26).
    signal:
        Signal-line EMA span (default 9).

    Returns
    -------
    tuple[pd.Series, pd.Series, pd.Series]
        ``(macd_line, signal_line, histogram)``
    """
    if fast >= slow:
        raise ValueError(f"fast ({fast}) must be < slow ({slow})")
    macd_line = ema(series, fast) - ema(series, slow)
    signal_line = ema(macd_line, signal)
    histogram = macd_line - signal_line
    return macd_line, signal_line, histogram


def stochastic(
    high: pd.Series,
    low: pd.Series,
    close: pd.Series,
    k_period: int = 14,
    d_period: int = 3,
) -> Tuple[pd.Series, pd.Series]:
    """Stochastic Oscillator (%K and %D).

    Parameters
    ----------
    high, low, close:
        OHLC high, low, and close series.
    k_period:
        Look-back for the %K calculation (default 14).
    d_period:
        SMA smoothing for %D (default 3).

    Returns
    -------
    tuple[pd.Series, pd.Series]
        ``(%K, %D)`` series in the range [0, 100].
    """
    lowest_low = low.rolling(window=k_period, min_periods=k_period).min()
    highest_high = high.rolling(window=k_period, min_periods=k_period).max()

    denom = highest_high - lowest_low
    # Avoid division-by-zero — where range is zero, %K is 50 (neutral).
    pct_k = 100.0 * (close - lowest_low) / denom.replace(0.0, np.nan)
    pct_k = pct_k.fillna(50.0)

    pct_d = pct_k.rolling(window=d_period, min_periods=d_period).mean()
    return pct_k, pct_d


def cci(
    high: pd.Series,
    low: pd.Series,
    close: pd.Series,
    period: int = 20,
) -> pd.Series:
    """Commodity Channel Index (CCI).

    Parameters
    ----------
    high, low, close:
        OHLC series.
    period:
        Look-back window (default 20).

    Returns
    -------
    pd.Series
        CCI values.  Values > +100 or < -100 indicate overbought / oversold.
    """
    tp = (high + low + close) / 3.0
    sma_tp = tp.rolling(window=period, min_periods=period).mean()
    mean_dev = tp.rolling(window=period, min_periods=period).apply(
        lambda x: float(np.abs(x - x.mean()).mean()), raw=True
    )
    return (tp - sma_tp) / (0.015 * mean_dev.replace(0.0, np.nan))


def mfi(
    high: pd.Series,
    low: pd.Series,
    close: pd.Series,
    volume: pd.Series,
    period: int = 14,
) -> pd.Series:
    """Money Flow Index (MFI) — volume-weighted RSI.

    Parameters
    ----------
    high, low, close:
        OHLC series.
    volume:
        Volume series.
    period:
        Look-back window (default 14).

    Returns
    -------
    pd.Series
        MFI values in the range [0, 100].
    """
    tp = (high + low + close) / 3.0
    raw_mf = tp * volume

    tp_shifted = tp.shift(1)
    positive_mf = pd.Series(
        np.where(tp > tp_shifted, raw_mf, 0.0),
        index=close.index,
        dtype=float,
    )
    negative_mf = pd.Series(
        np.where(tp < tp_shifted, raw_mf, 0.0),
        index=close.index,
        dtype=float,
    )

    pmf_sum = positive_mf.rolling(window=period, min_periods=period).sum()
    nmf_sum = negative_mf.rolling(window=period, min_periods=period).sum()

    ratio = pmf_sum / nmf_sum.replace(0.0, np.nan)
    mfi_vals = 100.0 - (100.0 / (1.0 + ratio))
    return mfi_vals


def williams_r(
    high: pd.Series,
    low: pd.Series,
    close: pd.Series,
    period: int = 14,
) -> pd.Series:
    """Williams %R — momentum oscillator similar to Stochastic.

    Parameters
    ----------
    high, low, close:
        OHLC series.
    period:
        Look-back window (default 14).

    Returns
    -------
    pd.Series
        Values range from -100 (oversold) to 0 (overbought).
    """
    highest_high = high.rolling(window=period, min_periods=period).max()
    lowest_low = low.rolling(window=period, min_periods=period).min()
    denom = highest_high - lowest_low
    return -100.0 * (highest_high - close) / denom.replace(0.0, np.nan)


