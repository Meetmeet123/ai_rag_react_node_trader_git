def vwap(
    high: pd.Series,
    low: pd.Series,
    close: pd.Series,
    volume: pd.Series,
) -> pd.Series:
    """Volume-Weighted Average Price (VWAP) — cumulative since series start.

    Parameters
    ----------
    high, low, close:
        OHLC series.
    volume:
        Volume series.

    Returns
    -------
    pd.Series
        Cumulative VWAP.  Resets automatically if you call this on a
        daily slice of intraday data.
    """
    typical_price = (high + low + close) / 3.0
    cum_tp_vol = (typical_price * volume).cumsum()
    cum_vol = volume.cumsum()
    return cum_tp_vol / cum_vol.replace(0.0, np.nan)


def obv(close: pd.Series, volume: pd.Series) -> pd.Series:
    """On-Balance Volume (OBV) — cumulative volume flow.

    Parameters
    ----------
    close:
        Close price series.
    volume:
        Volume series.

    Returns
    -------
    pd.Series
        Cumulative OBV values.
    """
    obv_vals = pd.Series(index=close.index, dtype=float)
    if len(close) == 0:
        return obv_vals

    obv_vals.iloc[0] = float(volume.iloc[0])
    close_shifted = close.shift(1)

    for i in range(1, len(close)):
        if close.iloc[i] > close_shifted.iloc[i]:
            obv_vals.iloc[i] = obv_vals.iloc[i - 1] + float(volume.iloc[i])
        elif close.iloc[i] < close_shifted.iloc[i]:
            obv_vals.iloc[i] = obv_vals.iloc[i - 1] - float(volume.iloc[i])
        else:
            obv_vals.iloc[i] = obv_vals.iloc[i - 1]

    return obv_vals


def vwma(
    close: pd.Series,
    volume: pd.Series,
    period: int = 20,
) -> pd.Series:
    """Volume-Weighted Moving Average (VWMA).

    Parameters
    ----------
    close:
        Close price series.
    volume:
        Volume series.
    period:
        Look-back window (default 20).

    Returns
    -------
    pd.Series
        VWMA values.
    """
    tp_vol = close * volume
    return tp_vol.rolling(window=period, min_periods=period).sum() / volume.rolling(window=period, min_periods=period).sum().replace(0.0, np.nan)


