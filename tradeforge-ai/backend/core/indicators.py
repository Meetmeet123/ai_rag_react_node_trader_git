"""
TradeForge AI — Pure-Function Technical Indicator Library.

Every indicator is implemented as a stateless pure function that accepts
Pandas Series (or DataFrames) and returns a new Series.  This makes them
naturally composable, testable, and safe for use inside strategy code
generated by the LLM.

All functions handle edge cases (insufficient data, NaN propagation) through
the underlying Pandas rolling-window machinery.
"""

from __future__ import annotations

from typing import Tuple

import numpy as np
import pandas as pd
from loguru import logger


# ---------------------------------------------------------------------------
# Moving Averages
# ---------------------------------------------------------------------------


def sma(series: pd.Series, period: int) -> pd.Series:
    """Simple Moving Average (SMA).

    Parameters
    ----------
    series:
        Input price series.
    period:
        Look-back window length (must be >= 1).

    Returns
    -------
    pd.Series
        SMA values aligned to the input index.  Leading ``period-1`` values
        are ``NaN``.
    """
    if period < 1:
        raise ValueError(f"period must be >= 1, got {period}")
    return series.rolling(window=period, min_periods=period).mean()


def ema(series: pd.Series, period: int) -> pd.Series:
    """Exponential Moving Average (EMA).

    Parameters
    ----------
    series:
        Input price series.
    period:
        Span for the exponential weighting.

    Returns
    -------
    pd.Series
        EMA values.  The first ``period`` values gradually converge.
    """
    if period < 1:
        raise ValueError(f"period must be >= 1, got {period}")
    return series.ewm(span=period, adjust=False, min_periods=period).mean()


def wma(series: pd.Series, period: int) -> pd.Series:
    """Weighted Moving Average (WMA) with linearly increasing weights.

    Parameters
    ----------
    series:
        Input price series.
    period:
        Look-back window length.

    Returns
    -------
    pd.Series
        WMA values.  Leading ``period-1`` values are ``NaN``.
    """
    if period < 1:
        raise ValueError(f"period must be >= 1, got {period}")
    weights = np.arange(1, period + 1, dtype=float)

    def _wma_window(vals: np.ndarray) -> float:
        return float(np.dot(vals, weights) / weights.sum())

    return series.rolling(window=period, min_periods=period).apply(
        _wma_window, raw=True
    )


def hma(series: pd.Series, period: int) -> pd.Series:
    """Hull Moving Average (HMA) — reduced lag moving average.

    Parameters
    ----------
    series:
        Input price series.
    period:
        Look-back window length.

    Returns
    -------
    pd.Series
        HMA values.
    """
    if period < 1:
        raise ValueError(f"period must be >= 1, got {period}")
    half_period = period // 2
    sqrt_period = int(np.sqrt(period))
    raw = 2.0 * ema(series, half_period) - ema(series, period)
    return ema(raw, sqrt_period)


